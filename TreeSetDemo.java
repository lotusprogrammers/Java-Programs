// Demonstrate TreeSet.
import java.util.*;
class TreeSetDemo {
	public static void main(String args[]) {
		// Create a TreeSet.
		TreeSet<String> ts = new TreeSet<String>();
		// Add elements to the Linked hash set.
		ts.add("Beta");
		ts.add("Alpha");
		ts.add("Eta");
		ts.add("Gamma");
		ts.add("Epsilon");
		ts.add("Omega");
		System.out.println(ts);
	}
}
